-- create student_info, course_info, and student_unit tables in osclr_db database
USE osclr_db;

CREATE TABLE course_info (
    Course_Code VARCHAR(10) NOT NULL,
    Course_Title VARCHAR(255),
    Year_Start_Offer VARCHAR(4),
	Year_End_Offer VARCHAR(4) NULL,
    Course_Coordinator_Name VARCHAR(255) NULL,
    Course_Coordinator_Email VARCHAR(255) NULL,
    Course_Coordinator_Mobile_Phone VARCHAR(10) NULL,
    PRIMARY KEY (Course_Code)
);

CREATE TABLE student_info (
    Person_Id INT NOT NULL,
    First_Name VARCHAR(255),
    Last_Name VARCHAR(255),
	Email VARCHAR(255),
    Mobile_Phone VARCHAR(10),
    Course_Code VARCHAR(10),
    Unit_Attempted INT,
    Unit_Completed INT,
    Course_Status VARCHAR(11),
    PRIMARY KEY (Person_Id),
    FOREIGN KEY (Course_Code) REFERENCES course_info(Course_Code)
);

CREATE TABLE student_unit (
    Record_No INT NOT NULL,
    Person_Id INT,
    Unit_Code VARCHAR(7),
    Unit_Title VARCHAR(255),
	Result_Score REAL,
    Result_Grade VARCHAR(2),
    Year_Attempted VARCHAR(4),
    Semester_Attempted VARCHAR(10),
    Notes VARCHAR(255),
    PRIMARY KEY (Record_No),
    FOREIGN KEY (Person_Id) REFERENCES student_info(Person_Id)
);

